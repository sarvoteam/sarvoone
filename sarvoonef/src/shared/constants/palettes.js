export const getPaletteStyles = (paletteId) => {
  const allPalettes = PALETTE_CATEGORIES.flatMap(cat => cat.palettes);
  const palette = allPalettes.find(p => p.id === paletteId) || allPalettes[0];
  
  return {
    '--primary-color': palette.primary,
    '--primary-gradient': palette.gradientStart === palette.gradientEnd 
      ? palette.gradientStart 
      : `linear-gradient(135deg, ${palette.gradientStart} 0%, ${palette.gradientEnd} 100%)`,
    '--hero-gradient': palette.heroStart.startsWith('linear-gradient') 
      ? palette.heroStart 
      : `linear-gradient(135deg, ${palette.heroStart} 0%, ${palette.heroEnd} 100%)`,
    '--button-gradient': `linear-gradient(135deg, ${palette.buttonStart} 0%, ${palette.buttonEnd} 100%)`,
    '--button-hover-gradient': `linear-gradient(135deg, ${palette.buttonHoverStart} 0%, ${palette.buttonHoverEnd} 100%)`,
    '--price-color': palette.priceColor,
    '--badge-bg': palette.badgeBg,
    '--badge-color': palette.badgeColor,
    '--badge-border': palette.badgeBorder,
    '--summary-bg': palette.summaryBg,
    '--summary-border': palette.summaryBorder,
    '--summary-color': palette.summaryColor,
    '--shadow-color': palette.shadowColor
  };
};
